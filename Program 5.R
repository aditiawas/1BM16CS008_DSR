library(visualize)
library(BSDA)
x <- rnorm(10,mean=100,sd=5)
y <- rnorm(20,mean=105,sd=5)
t.test(x,y,var.equal=TRUE)
qt(p=0.05/2,df=28,lower.tail=FALSE)
opt=par(mfrow=c(1,2))
visualize.t(stat=c(-3.3343,3.3343),df=28,section="tails")
visualize.t(stat=c(-2.048407,2.048407),df=28,section="tails")